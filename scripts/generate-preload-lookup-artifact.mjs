import { mkdir, readFile, writeFile } from 'node:fs/promises';
import os from 'node:os';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

import {
  DETERMINISTIC_LOOKUP_WARMUP_SPEC,
  buildDeterministicLookupPlan,
  computeDeterministicLookupChecksum,
} from '../src/preload/deterministicLookupWarmup.js';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, '..');
const outputPath = path.join(repoRoot, 'src', 'generated', 'preloadLookupArtifact.js');

const plan = buildDeterministicLookupPlan();
const checksum = computeDeterministicLookupChecksum(plan);

const content = [
  '// Generated by scripts/generate-preload-lookup-artifact.mjs.',
  'export const PRELOAD_LOOKUP_ARTIFACT = Object.freeze({',
  `  checksum: ${checksum},`,
  `  sampleCount: ${plan.length},`,
  `  gridRadius: ${DETERMINISTIC_LOOKUP_WARMUP_SPEC.gridRadius},`,
  `  step: ${DETERMINISTIC_LOOKUP_WARMUP_SPEC.step},`,
  '});',
].join(os.EOL) + os.EOL;

await mkdir(path.dirname(outputPath), { recursive: true });

let current = null;
try {
  current = await readFile(outputPath, 'utf8');
} catch {
  current = null;
}

if (current !== content) {
  await writeFile(outputPath, content, 'utf8');
  console.log(`Updated ${path.relative(repoRoot, outputPath)}`);
} else {
  console.log(`Unchanged ${path.relative(repoRoot, outputPath)}`);
}